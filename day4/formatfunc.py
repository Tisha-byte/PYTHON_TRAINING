#default arguments
print("Hello {} your balance is {}".format("Ram",230.2346))
#positional arguments
print("Hello {0} your balance is {1}".format("Ram",230.2346))
#keyword argument
print("Hello {name} your balance is {blc}".format(name="Ram",blc=230.2346))
#mixed argument
print("Hello {0} your balance is {blc}".format("Ram",blc=230.2346))
